# Faça um programa que leia algo
# pelo teclado e mostre na tela o seu tipo primitivo
# e todas as informações possiveis sobre ele

variavel = input('Digite algo: ')
print(type(variavel), variavel, variavel.isalnum())
print(type(variavel), variavel, variavel.isalpha())
print(type(variavel), variavel, variavel.isdigit())
print(type(variavel), variavel, variavel.isupper())
print(type(variavel), variavel, variavel.islower())
print(type(variavel), variavel, variavel.isnumeric())
